package user;

public class User {

    String userName;
    int userAge;
    String unserGender;
    String userEmail;

    public String getUserName() {
        return userName;
    }

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public int getUserAge() {
        return userAge;
    }

    public void setUserAge(int userAge) {
        this.userAge = userAge;
    }

    public String getUnserGender() {
        return unserGender;
    }

    public void setUnserGender(String unserGender) {
        this.unserGender = unserGender;
    }

    public String getUserEmail() {
        return userEmail;
    }

    public void setUserEmail(String userEmail) {
        this.userEmail = userEmail;
    }
}
